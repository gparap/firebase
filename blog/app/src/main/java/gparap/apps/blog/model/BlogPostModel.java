package gparap.apps.blog.model;

/**
 * Model class for a blog post.
 */
@SuppressWarnings("unused")
public class BlogPostModel {
    private String imageUrl, title, details, userId, username;

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    //required for Firebase
    public BlogPostModel() {
    }

    public BlogPostModel(String imageUrl, String title, String details, String userId) {
        this.imageUrl = imageUrl;
        this.title = title;
        this.details = details;
        this.userId = userId;
        this.username = ""; //set after searching for user before posting
    }
}
