package gparap.apps.social_media.data;

/**
 * This is a model class for a user post.
 */
@SuppressWarnings("unused")
public class PostModel {
    private String id, userId, title, details, imageUrl;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    //!!! required for Firebase
    public PostModel() {
    }

    public PostModel(String id, String userId, String title, String details, String imageUrl) {
        this.id = id;
        this.userId = userId;
        this.title = title;
        this.details = details;
        this.imageUrl = imageUrl;
    }
}
